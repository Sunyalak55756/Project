package com.example.project2020.ListItem4adapt;


public class listItem {
    String head;
    String dest;
    String test;
    public listItem(String head,String dest,String test){
        this.head = head;
        this.dest = dest;
        this.test = test;
    }
    public String getHead(){
        return head;
    }
    public String getDesc(){
        return dest;
    }
    public String getType(){
        return test;
    }
    }

